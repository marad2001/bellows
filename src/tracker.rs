/// Parsed shape of the `**Blocked by:**` line in an agent brief.
/// The polling-loop re-loop sweep (ADR-0007) branches on this variant
/// when deciding whether to strip the `blocked-by` label.
///
/// - `NoBlockers`: the brief is present and either explicitly says
///   `None` (with or without a verbose annotation), has no
///   `**Blocked by:**` line at all, or only carries unparseable /
///   cross-repo references that the v1 parser cannot verify.
/// - `Blockers(nums)`: the brief lists one or more parseable
///   same-repo `#N` references. The re-loop checks each `N`'s state
///   and strips the label only when every one is `CLOSED`.
/// - `Unverifiable`: the input is not an agent brief at all (the
///   `## Agent Brief` header is absent). The caller leaves the
///   `blocked-by` label in place and logs a warning naming the
///   issue.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum BlockedBySection {
    NoBlockers,
    Blockers(Vec<u64>),
    Unverifiable,
}

/// Parse the `**Blocked by:**` line out of an agent brief body.
///
/// Cases handled (ADR-0007 / brief #116):
/// - `**Blocked by:** #95` → `Blockers([95])`
/// - `**Blocked by:** #95, #96` → `Blockers([95, 96])`
/// - `**Blocked by:** #95 (rationale)` → `Blockers([95])`
/// - `**Blocked by:** None` → `NoBlockers`
/// - `**Blocked by:** None — verbose tail` → `NoBlockers`
/// - missing `**Blocked by:**` line → `NoBlockers`
/// - cross-repo `owner/name#N` → ignored (logged via stderr); when
///   no parseable same-repo tokens remain → `NoBlockers`
/// - malformed `#NaN`, bare `abc`, `#` → ignored; when nothing
///   parseable remains → `NoBlockers`
/// - missing `## Agent Brief` header entirely → `Unverifiable`
pub fn parse_blocked_by_section(brief_body: &str) -> BlockedBySection {
    if !brief_body.contains("## Agent Brief") {
        return BlockedBySection::Unverifiable;
    }

    // Find the line beginning with `**Blocked by:**` (case-sensitive
    // — this is the shape `/triage` emits). Anywhere on a line so
    // leading whitespace / list-bullets don't break the match.
    let prefix = "**Blocked by:**";
    let Some(line) = brief_body.lines().find_map(|l| {
        let trimmed = l.trim_start();
        trimmed.strip_prefix(prefix).map(|rest| rest.trim())
    }) else {
        return BlockedBySection::NoBlockers;
    };

    // Drop any trailing parenthetical annotation (`#95 (rationale)`
    // → `#95`). We only need to strip a single trailing `(...)` —
    // the line shape is "one or more refs, optionally followed by a
    // human-readable note in parens".
    let body = match line.find('(') {
        Some(idx) => line[..idx].trim(),
        None => line,
    };

    // Explicit `None` (with or without a verbose annotation) clears
    // any blockers. We check this BEFORE the comma-split so
    // "None — can start immediately" matches even though the
    // em-dash tail isn't a token.
    let body_lower = body.to_ascii_lowercase();
    if body_lower == "none"
        || body_lower.starts_with("none ")
        || body_lower.starts_with("none\t")
        || body_lower.starts_with("none.")
        || body_lower.starts_with("none,")
        || body_lower.starts_with("none—")
        || body_lower.starts_with("none–")
        || body_lower.starts_with("none-")
    {
        return BlockedBySection::NoBlockers;
    }

    // Split on commas; each token must be `#N` where N parses as
    // u64. Cross-repo `owner/name#N` and bare malformed tokens are
    // logged to stderr and dropped from the result.
    let mut blockers = Vec::new();
    for raw in body.split(',') {
        let token = raw.trim();
        if token.is_empty() {
            continue;
        }
        if token.contains('/') {
            eprintln!(
                "bellows: ignoring cross-repo blocker reference `{}` in **Blocked by:** line (v1 is same-repo only; see ADR-0007)",
                token,
            );
            continue;
        }
        let Some(num_str) = token.strip_prefix('#') else {
            eprintln!(
                "bellows: ignoring malformed blocker token `{}` in **Blocked by:** line (expected `#N`)",
                token,
            );
            continue;
        };
        match num_str.parse::<u64>() {
            Ok(n) => blockers.push(n),
            Err(_) => {
                eprintln!(
                    "bellows: ignoring malformed blocker token `#{}` in **Blocked by:** line (not a u64)",
                    num_str,
                );
            }
        }
    }

    if blockers.is_empty() {
        BlockedBySection::NoBlockers
    } else {
        BlockedBySection::Blockers(blockers)
    }
}

/// Look up the latest agent-brief comment on an issue (the one whose body
/// includes the `## Agent Brief` header that `/triage` posts). Returns
/// `Ok(None)` if no such comment exists.
pub async fn fetch_agent_brief(
    client: &octocrab::Octocrab,
    owner: &str,
    repo: &str,
    issue_number: u64,
) -> Result<Option<String>, octocrab::Error> {
    let route = format!("/repos/{owner}/{repo}/issues/{issue_number}/comments");
    let params = ListCommentsParams { per_page: 100 };
    let comments: Vec<Comment> = client.get(&route, Some(&params)).await?;

    Ok(comments
        .into_iter()
        .filter_map(|c| c.body)
        .rfind(|b| b.contains("## Agent Brief")))
}

/// Inputs for `finalise`. Bundled into a struct rather than passed as
/// 8 positional arguments — clippy's too_many_arguments threshold and
/// readability both improve.
pub struct FinaliseRequest<'a> {
    pub owner: &'a str,
    pub repo: &'a str,
    pub issue_number: u64,
    pub pr_number: u64,
    pub in_progress_label: &'a str,
    pub outcome_label: &'a str,
    pub log_body: &'a str,
}

/// Result of `finalise`. Carries the post-transition issue plus a flag
/// indicating whether the in-progress label was already gone before
/// finalise ran — the slice-10 `bellows kill <N>` operator path: when
/// the operator transitioned the label out from under the running
/// orchestrator, finalise still posts the log comment so the
/// abandoned-mid-run state is recorded, but skips the PATCH (the
/// operator's label is already correct) and signals back so run_once
/// can return `RunOutcome::Cancelled` instead of `RunOutcome::Finalised`.
///
/// `comment_post_failure` (issue #87) carries the formatted API error
/// when the run-log comment POST itself failed — typically a 422
/// because the body exceeds GitHub's 64 KiB hard limit, but also any
/// transient 5xx / network blip on the comments endpoint. The label
/// transition runs BEFORE the comment post, so this field being `Some`
/// means the state machine reached its terminal label correctly and
/// only the observability comment is missing; the caller surfaces this
/// as an operator-visible log warning rather than treating it as a
/// pipeline-fatal error.
pub struct FinaliseOutcome {
    pub issue: Issue,
    pub externally_cancelled: bool,
    pub comment_post_failure: Option<String>,
}

/// Lightweight GET on an issue's current labels to check whether
/// `in_progress_label` is still present. Used by `run_once` BEFORE
/// opening the PR to detect mid-run cancellation by `bellows kill <N>`
/// (slice 10) — without this check, the cancellation is only detected
/// in `finalise`, which is too late: the PR has already been opened
/// (potentially as ready-for-review with a "Success" log header) and
/// post_pr_comment has already posted the findings comment.
///
/// Returns `Ok(true)` if the in-progress label is still on the issue,
/// `Ok(false)` if it's been removed externally (operator cancelled).
/// Errors propagate as-is — the runner treats fetch failures as
/// "assume still in progress" (best-effort, doesn't block the pipeline).
pub async fn issue_in_progress(
    client: &octocrab::Octocrab,
    owner: &str,
    repo: &str,
    issue_number: u64,
    in_progress_label: &str,
) -> Result<bool, octocrab::Error> {
    let route = format!("/repos/{owner}/{repo}/issues/{issue_number}");
    let current: Issue = client.get(&route, None::<&()>).await?;
    Ok(current.labels.iter().any(|l| l.name == in_progress_label))
}

/// Transition the issue's labels from `in_progress_label` to
/// `outcome_label`, then post the run-log comment on the PR. Generic
/// over the outcome — the caller decides whether the run was a success
/// (`agent-done`), a failure (`agent-failed`), or one of the more
/// specific failure kinds added in later slices.
///
/// **Step order matters (issue #87).** The label PATCH runs BEFORE the
/// comment POST so the issue's state machine reaches its terminal
/// label even if the comment endpoint fails — historically the order
/// was reversed and a 422 on the comment (body exceeds GitHub's 64 KiB
/// hard limit) left the source issue stuck at `agent-in-progress`,
/// blocking the polling loop on the open `agent/*` PR. With this
/// ordering the comment post is observability-only: a failure is
/// surfaced via `FinaliseOutcome::comment_post_failure` (so the
/// caller can write a log warning) but does NOT propagate as `Err`
/// to `run_once`. Any subsequent post-label failure (transient 5xx,
/// network blip, rate limit on the comments endpoint) is handled the
/// same way.
///
/// If the GET shows that `in_progress_label` is no longer on the issue
/// (the slice-10 `bellows kill <N>` operator path), the comment is still
/// attempted but the label PATCH is skipped — the operator's manual
/// transition has already moved the issue to the cancelled state, and a
/// second PATCH would clobber that.
pub async fn finalise(
    client: &octocrab::Octocrab,
    req: FinaliseRequest<'_>,
) -> Result<FinaliseOutcome, octocrab::Error> {
    let issue_route = format!(
        "/repos/{owner}/{repo}/issues/{issue_number}",
        owner = req.owner,
        repo = req.repo,
        issue_number = req.issue_number,
    );
    let current: Issue = client.get(&issue_route, None::<&()>).await?;

    let externally_cancelled = !current.labels.iter().any(|l| l.name == req.in_progress_label);
    let issue_after_patch = if externally_cancelled {
        current
    } else {
        let mut new_labels: Vec<String> = current
            .labels
            .iter()
            .map(|l| l.name.clone())
            .filter(|n| n != req.in_progress_label)
            .collect();
        new_labels.push(req.outcome_label.to_string());
        new_labels.sort();

        let body = serde_json::json!({ "labels": new_labels });
        client.patch(&issue_route, Some(&body)).await?
    };

    // Comment POST is observability-only — a failure here (e.g. 422
    // body-too-long, transient 5xx) must NOT roll back the label
    // transition above or propagate as `Err` to the runner. Surface the
    // failure via `comment_post_failure` so the caller can log an
    // operator-visible warning instead.
    let comment_route = format!(
        "/repos/{owner}/{repo}/issues/{pr_number}/comments",
        owner = req.owner,
        repo = req.repo,
        pr_number = req.pr_number,
    );
    // Defensive cap (issue #87) — `runner::build_log_body` already
    // bounds the body, but `finalise` is the GitHub API boundary so we
    // re-apply the truncation here. The helper passes short bodies
    // through unchanged, so this is a no-op on the common path.
    let bounded_body = truncate_for_github_comment(req.log_body);
    let comment_body = serde_json::json!({ "body": bounded_body });
    let post_result: Result<serde_json::Value, octocrab::Error> =
        client.post(&comment_route, Some(&comment_body)).await;
    let comment_post_failure = post_result.err().map(|e| e.to_string());

    Ok(FinaliseOutcome {
        issue: issue_after_patch,
        externally_cancelled,
        comment_post_failure,
    })
}

/// GitHub-side handler for `bellows kill <N>`. Posts a short
/// AI-disclaimer-style cancellation comment so a human reading the
/// issue knows what happened, then transitions the issue's labels by
/// removing `from_label` (typically `agent-in-progress`) and adding
/// `to_label` (typically `agent-cancelled`). Sibling of `finalise` —
/// same comment-then-label-swap shape, but with a fixed comment body
/// rather than the runner's per-phase log summary.
pub async fn transition_to_cancelled(
    client: &octocrab::Octocrab,
    owner: &str,
    repo: &str,
    issue_number: u64,
    from_label: &str,
    to_label: &str,
) -> Result<Issue, octocrab::Error> {
    let now = chrono::Utc::now();
    let comment_body = format!(
        "> *bellows: cancelled by operator at {}*",
        now.to_rfc3339(),
    );

    let comment_route = format!("/repos/{owner}/{repo}/issues/{issue_number}/comments");
    let payload = serde_json::json!({ "body": comment_body });
    let _: serde_json::Value = client.post(&comment_route, Some(&payload)).await?;

    let issue_route = format!("/repos/{owner}/{repo}/issues/{issue_number}");
    let current: Issue = client.get(&issue_route, None::<&()>).await?;

    let mut new_labels: Vec<String> = current
        .labels
        .iter()
        .map(|l| l.name.clone())
        .filter(|n| n != from_label)
        .collect();
    if !new_labels.iter().any(|n| n == to_label) {
        new_labels.push(to_label.to_string());
    }
    new_labels.sort();

    let body = serde_json::json!({ "labels": new_labels });
    let updated: Issue = client.patch(&issue_route, Some(&body)).await?;
    Ok(updated)
}

/// Post a freestanding comment on an issue or PR (PRs share the issues
/// comments endpoint on GitHub). Used by the runner to surface the
/// review-phase findings file as a `## Review findings` comment without
/// the label-swap baked into `finalise`.
pub async fn post_pr_comment(
    client: &octocrab::Octocrab,
    owner: &str,
    repo: &str,
    pr_number: u64,
    body: &str,
) -> Result<(), octocrab::Error> {
    let route = format!("/repos/{owner}/{repo}/issues/{pr_number}/comments");
    let payload = serde_json::json!({ "body": body });
    let _: serde_json::Value = client.post(&route, Some(&payload)).await?;
    Ok(())
}

