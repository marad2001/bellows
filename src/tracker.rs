use crate::triage::{TriageVerdict, VerdictState};

/// The canonical AI-generated-during-triage disclaimer bellows
/// prepends to every comment posted as part of an applied verdict.
/// Matches the disclaimer the manual `/triage` skill uses so the two
/// produce visually identical comments on the issue.
pub const TRIAGE_AI_DISCLAIMER: &str = "> *This was generated by AI during triage.*";

/// Issue #87: cap below which we hand a comment body off to the
/// GitHub API. The hard API limit is 65536 characters; we clip well
/// short of that to leave headroom for any encoding / HTML / details-
/// tag wrapping the API might apply server-side. Bodies that would
/// exceed this cap are clipped by `truncate_for_github_comment` with
/// a footer pointing the operator at `bellows.log` for the full
/// output.
pub const GITHUB_COMMENT_MAX_CHARS: usize = 60_000;

/// Truncation footer appended by `truncate_for_github_comment` when
/// the input exceeds `GITHUB_COMMENT_MAX_CHARS`. The leading triple-
/// backtick line closes whichever code fence the run-log body's
/// `### Agent output tail` / `### \`cargo …\` output` block likely
/// left open (those code blocks carry the bulk of the byte weight,
/// so the truncation point is in practice almost always inside one
/// of them). The italicised text is the operator-visible marker —
/// `bellows.log` is the host-side file the truncated content lives
/// in. The trailing `</details>` re-closes the wrapping HTML element
/// that `build_log_body` emits so the rendered comment stays
/// well-formed.
const COMMENT_TRUNCATION_FOOTER: &str =
    "\n```\n\n_(truncated; full output in bellows.log on the operator's host)_\n\n</details>";

/// Bound `body` at `GITHUB_COMMENT_MAX_CHARS`, appending a truncation
/// footer when the input exceeds the cap. Bodies already within the
/// limit pass through unchanged.
///
/// Issue #87: large agent runs (huge stderr tails, multi-thousand-line
/// cargo output) produce run-log comment bodies above GitHub's 64 KiB
/// hard limit on issue/PR comment bodies. Posting such a body returns
/// HTTP 422 which historically collapsed the whole `finalise` step and
/// stranded the source issue at `agent-in-progress`. This helper is
/// the single source of truth for the cap — `runner::build_log_body`
/// calls it before returning, and `tracker::finalise` calls it
/// defensively on its `log_body` argument so the API-boundary contract
/// holds regardless of caller-side size-awareness.
pub fn truncate_for_github_comment(body: &str) -> String {
    if body.chars().count() <= GITHUB_COMMENT_MAX_CHARS {
        return body.to_string();
    }
    let footer_chars = COMMENT_TRUNCATION_FOOTER.chars().count();
    let keep_chars = GITHUB_COMMENT_MAX_CHARS.saturating_sub(footer_chars);
    let prefix: String = body.chars().take(keep_chars).collect();
    format!("{prefix}{COMMENT_TRUNCATION_FOOTER}")
}

/// All five canonical triage-state label strings — the set
/// `apply_verdict` strips off the issue's existing labels before
/// adding the new verdict's state label. Mirrors
/// `docs/agents/triage-labels.md`.
const CANONICAL_TRIAGE_LABELS: &[&str] = &[
    "needs-triage",
    "needs-info",
    "ready-for-agent",
    "ready-for-human",
    "wontfix",
];

#[derive(Debug, Clone, Deserialize)]
pub struct Issue {
    pub number: u64,
    pub title: String,
    #[serde(default)]
    pub labels: Vec<Label>,
    /// GitHub's `created_at` timestamp for the issue. Used by the
    /// multi-repo polling loop (issue #35) to break ties across repos
    /// — the oldest-by-created_at issue across all configured repos is
    /// claimed first. Optional in the deserializer so existing tests
    /// and any future caller that only cares about
    /// `number`/`title`/`labels` keep parsing minimal payloads; real
    /// GitHub responses always include the field.
    #[serde(default)]
    pub created_at: Option<chrono::DateTime<chrono::Utc>>,
}

/// Read-only bundle of issue state the triage agent sees in
/// `/workspace/.bellows-triage-input.md`. Carries the issue body,
/// the title (so the agent doesn't have to infer from the body),
/// the current label set, and the first 100 ordered comments.
/// Order matters: a prior triage `needs-info` note may have asked a
/// question that a later reporter comment answers — the agent must
/// see the answer-after-the-question sequence to reach the right
/// verdict on re-triage. On very long issues (>100 comments) the
/// newest comments past the page-1 cap may not be visible to the
/// agent; see `fetch_issue_with_comments` for the rationale.
#[derive(Debug, Clone)]
pub struct IssueBundle {
    pub number: u64,
    pub title: String,
    pub body: Option<String>,
    pub labels: Vec<String>,
    pub comments: Vec<String>,
}

#[derive(Debug, Deserialize)]
struct IssueWithBody {
    number: u64,
    title: String,
    #[serde(default)]
    body: Option<String>,
    #[serde(default)]
    labels: Vec<Label>,
}

#[derive(Debug, Deserialize)]
struct IssueComment {
    body: Option<String>,
}

#[derive(serde::Serialize)]
struct ListIssueCommentsParams {
    per_page: u32,
}

/// Fetch the issue body, labels, title, and the first 100 comments
/// for triage. Used by `bellows triage <N>` to build the in-container
/// input bundle. The container has no GitHub credentials — bellows
/// owns this fetch on the host side and writes the result to a
/// workspace file the agent reads.
///
/// Comments come back in API-default order (oldest-first on the
/// public GitHub API), which is the order a human reading the issue
/// would see them. The `per_page=100` cap (with no follow-up
/// pagination) matches the rest of bellows's GitHub-list calls; on
/// the rare issue with >100 comments the newest comments past page 1
/// will not be visible to the triage agent.
pub async fn fetch_issue_with_comments(
    client: &octocrab::Octocrab,
    owner: &str,
    repo: &str,
    issue_number: u64,
) -> Result<IssueBundle, octocrab::Error> {
    let issue_route = format!("/repos/{owner}/{repo}/issues/{issue_number}");
    let issue: IssueWithBody = client.get(&issue_route, None::<&()>).await?;

    let comments_route = format!("/repos/{owner}/{repo}/issues/{issue_number}/comments");
    let comments_params = ListIssueCommentsParams { per_page: 100 };
    let comments: Vec<IssueComment> = client.get(&comments_route, Some(&comments_params)).await?;

    Ok(IssueBundle {
        number: issue.number,
        title: issue.title,
        body: issue.body,
        labels: issue.labels.into_iter().map(|l| l.name).collect(),
        comments: comments.into_iter().filter_map(|c| c.body).collect(),
    })
}

/// Apply a parsed-and-validated triage verdict to the GitHub issue.
/// Posts the AI-disclaimer-prefixed `comment_body`, posts the
/// `## Agent Brief` / `## Human Brief` payload as a separate comment
/// when relevant, and PATCHes the issue: replacing whichever
/// canonical triage label was on the issue with the new state's
/// label, and (for `wontfix`) also setting `state: closed`.
///
/// The PATCH is a single request that updates both labels and (when
/// closing) the issue's open/closed state — half-applied verdicts on
/// a transient network failure are easier to reason about when one
/// PATCH does the whole transition. Other labels on the issue (e.g.
/// `bug`, `enhancement`) are preserved.
///
/// The workspace-side wontfix-enhancement path (writing
/// `.out-of-scope/<filename>.md` to master) is handled by the caller
/// via `workspace::commit_to_branch` BEFORE invoking this function so
/// the closing comment can reference a file that already exists on
/// master.
pub async fn apply_verdict(
    client: &octocrab::Octocrab,
    owner: &str,
    repo: &str,
    issue_number: u64,
    verdict: &TriageVerdict,
) -> Result<(), octocrab::Error> {
    // Step 1: read the issue's current labels so we can recompute the
    // new label set (drop any of the five canonical triage labels,
    // preserve everything else, add the verdict's state label).
    let issue_route = format!("/repos/{owner}/{repo}/issues/{issue_number}");
    let current: Issue = client.get(&issue_route, None::<&()>).await?;
    let mut new_labels: Vec<String> = current
        .labels
        .iter()
        .map(|l| l.name.clone())
        .filter(|n| !CANONICAL_TRIAGE_LABELS.contains(&n.as_str()))
        .collect();
    new_labels.push(verdict.state.label().to_string());
    new_labels.sort();
    new_labels.dedup();

    // Step 2: post the main verdict comment with the AI disclaimer.
    let comments_route = format!("/repos/{owner}/{repo}/issues/{issue_number}/comments");
    let main_body = format!("{}\n\n{}", TRIAGE_AI_DISCLAIMER, verdict.comment_body);
    let payload = serde_json::json!({ "body": main_body });
    let _: serde_json::Value = client.post(&comments_route, Some(&payload)).await?;

    // Step 3: when the verdict carries a separate brief (agent or
    // human), post it as its own comment so it can be located by
    // header (the downstream bellows-run pipeline scans for the
    // literal `## Agent Brief` header via fetch_agent_brief).
    let brief_comment_body = match verdict.state {
        VerdictState::ReadyForAgent => verdict
            .agent_brief
            .as_ref()
            .map(|b| format!("{}\n\n{}", TRIAGE_AI_DISCLAIMER, b)),
        VerdictState::ReadyForHuman => verdict
            .human_brief
            .as_ref()
            .map(|b| format!("{}\n\n{}", TRIAGE_AI_DISCLAIMER, b)),
        VerdictState::NeedsInfo | VerdictState::Wontfix => None,
    };
    if let Some(body) = brief_comment_body {
        let payload = serde_json::json!({ "body": body });
        let _: serde_json::Value = client.post(&comments_route, Some(&payload)).await?;
    }

    // Step 4: PATCH the issue. For wontfix verdicts, close the issue
    // in the same PATCH as the label swap so the GitHub-side state is
    // updated atomically (one request → consistent post-state).
    let mut patch = serde_json::Map::new();
    patch.insert("labels".into(), serde_json::json!(new_labels));
    if verdict.state == VerdictState::Wontfix {
        patch.insert("state".into(), serde_json::json!("closed"));
    }
    let _: serde_json::Value = client.patch(&issue_route, Some(&patch)).await?;

    Ok(())
}

